
let first = 2;
let second = "2";

/*check with double equal*/
if (first == second) {
    console.log("condition is true");
}
else {
    console.log("condition is false");
}
//output "condition is true"


/*check with triple equal*/

if (first === second) {
    console.log("condition is true");
}
else {
    console.log("condition is false");
}
//here output "condition is false"